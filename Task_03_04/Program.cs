using System;

class Program
{ // * Пользователь вводить в консоли произвольный текст. После каждого ввода консоль очищается.
  // * Когда пользователь вводить слово «exit» или пустую строку – ввод останавливается и выводиться
// количество строк,введенных пользователем. 
    static void Main()
    {
        int lineCount = 0; // счётчик строк, которые ввёл пользователь
        string input; // переменная для хранения ввода пользователя

        // бесконечный цикл, предназначенный для непрерывного ввода
        while (true)
        {
            // нужно запросить ввод от пользователя
            Console.Write("Введите текст (или 'exit' для выхода): ");
            input = Console.ReadLine(); // Читаем ввод

            // проверка того, является ли ввод пустой строкой или словом "exit"
            if (string.IsNullOrEmpty(input) || input.Equals("exit", StringComparison.OrdinalIgnoreCase))
            {
                break; // выход из цикла, если выполнено одно из условий
            }

            lineCount++; // увеличение счётчика строк

            Console.Clear(); // очищаем консоль
        }

        // вывод количества введенных строк
        Console.WriteLine($"Количество введенных строк: {lineCount}");
    }
}
