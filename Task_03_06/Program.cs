using System;

class Program
{ // Написать программу, которая выводит таблицу значений функции: 𝑦=|𝑥|для -4≤x≤4, с шагом h = 0,5.
    static void Main()
    {
        // установка начального значения x
        double x = -4.0;

        // теперь задаём конечное значение x
        double endX = 4.0;

        // устанавливаем шаг из условия задачи
        double step = 0.5;

        // заголовок таблицы
        Console.WriteLine(" x   |  y = |x|");
        Console.WriteLine("-----|--------");

        // цикл для вычисления и вывода значений функции
        while (x <= endX)
        {
            // вычисление значения функции |x|
            double y = Math.Abs(x);

            // нужно вывести результат в виде таблицы
            Console.WriteLine($"{x,5:F1} | {y,7:F1}");

            // увеличиваем x на один шаг
            x += step;
        }
    }
}
