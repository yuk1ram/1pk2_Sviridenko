using System;

class Program
{ //Вклад в банке составляет x рублей. Ежегодно он увеличивается на p процентов, после чего дробная часть
  //копеек отбрасывается. Каждый год сумма вклада становится больше.Определите, через сколько лет вклад
  //составит не менее y рублей.

    static void Main()
    {
        // вводим начальный вклад, процентную ставку, целевую сумму
        Console.Write("Введите начальный вклад (x): ");
        double x = Convert.ToDouble(Console.ReadLine());

        Console.Write("Введите процентную ставку (p): ");
        double p = Convert.ToDouble(Console.ReadLine());

        Console.Write("Введите целевую сумму (u): ");
        double u = Convert.ToDouble(Console.ReadLine());

        // присваиваем начальное значение переменной для хранения количества лет
        int years = 0;

        // цикл для расчёта роста вклада
        while (x < u)
        {
            // проводим вычисление прироста вклада за год
            x += x * (p / 100);

            // теперь отбрасываем дробную часть копеек и сразу округляем до целого числа
            x = Math.Floor(x);

            // увеличиваем счётчик лет
            years++;
        }

        // вывод результата
        Console.WriteLine($"Вклад достигнет или превысит {u} через {years} лет.");
    }
}
